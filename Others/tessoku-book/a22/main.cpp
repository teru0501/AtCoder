// ================= STRATEGY ================= //
// 制約をみろ！！
// 愚直を考えろ！！
// オバフロ注意！！
// dp[i] iマス目まで進んだ時の最大値
// 与えるDP
// ============================================ //

void solve () {
  int n;
  cin >> n;
  vll a(n - 1);
  IN(a);
  vll b(n - 1);
  IN(b);
  vll dp(n + 1, -1);
  dp[1] = 0;
  for (int i = 1; i < n; i++) {
    if (dp[i] != -1) {
      chmax(dp[a[i - 1]], dp[i] + 100);
      chmax(dp[b[i - 1]], dp[i] + 150);
    }
  }
  if (dp[n] == -1) OUT(0);
  else OUT(dp[n]);
  return;
}

